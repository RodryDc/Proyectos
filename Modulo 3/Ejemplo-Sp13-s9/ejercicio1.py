"""Calcular el área de un rectángulo
Escribe una función llamada calcular_area_rectangulo() que reciba dos parámetros, el
ancho y el alto de un rectángulo, y calcule su área.
Entrada:
calcular_area_rectangulo(5, 10)
Salida:
50"""

def calcular_area_rectangulo(base, altura):
    resultado = base * altura
    return resultado

